// Configuration du provider Hardhat
const provider = new ethers.JsonRpcProvider('http://localhost:8545');

// Fonction pour obtenir une instance du contrat
async function getContract() {
  const signer = await provider.getSigner();
  return new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
}

function App() {
  const [contract, setContract] = React.useState<ethers.Contract | null>(null);
  const [loading, setLoading] = React.useState(true);
  const [error, setError] = React.useState<string | null>(null);

  React.useEffect(() => {
    const init = async () => {
      try {
        const contractInstance = await getContract();
        setContract(contractInstance);
      } catch (err) {
        console.error('Erreur lors de la connexion au contrat :', err);
        setError('Impossible de se connecter au contrat.');
      } finally {
        setLoading(false);
      }
    };
    init();
  }, []);

  if (loading) {
    return <div className="text-center py-8">Chargement du contrat...</div>;
  }

  if (error) {
    return <div className="text-center py-8 text-red-500">{error}</div>;
  }

  if (!contract) {
    return <div className="text-center py-8">Aucun contrat trouvé.</div>;
  }

  return (
    <Router>
      <div className="min-h-screen bg-gray-50">
        <Navigation />
        <div className="max-w-7xl mx-auto px-4 py-8">
          <Routes>
            <Route path="/" element={<Home contract={contract} />} />
            <Route path="/my-properties" element={<MyProperties contract={contract} />} />
            <Route path="/exchange" element={<Exchange contract={contract} />} />
            <Route path="/property/:id" element={<PropertyDetails contract={contract} />} />
          </Routes>
        </div>
      </div>
    </Router>
  );
}

export default App;